-- Stored Procedures are a powerful feature in SQL that allows you to encapsulate a set of SQL statements 
-- for reuse, maintainability, and performance optimization. They are essentially precompiled collections
--  of SQL statements that can be executed as a single unit.


#  Definition:
# -------------------------------------------------------------------------------------------------

-- A stored procedure is a saved collection of SQL queries that can be reused multiple times.
-- They are stored in the database and can be executed with a single call.

# Benefits:
#--------------------------------------------------------------------------------------------------

-- Reusability: Write once, use multiple times.
-- Maintainability: Easier to update and manage logic in one place.
-- Performance: Precompiled and optimized by the database engine.
-- Security: Can restrict direct access to data, exposing only necessary operations.




